public class Punto {
    int x = 0;
    private int y = 0;

    public Punto(){}

    public Punto(int i, int z){
        x=i;
        y=z;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
